/// The [CropDragPointPosition] defines the position of the crop drag points
/// in the image cropping widget.
enum CropDragPointPosition {
  /// The top left position of the crop area.
  topLeft,

  /// The top right position of the crop area.
  topRight,

  /// The bottom left position of the crop area.
  bottomLeft,

  /// The bottom right position of the crop area.
  bottomRight,
}
